export const getPopularSongs = (): Song[] => {
  return mockSongs.sort((a, b) => b.popularity - a.popularity).slice(0, 10);
};

export const getSongsByGenre = (genre: string): Song[] => {
  return mockSongs.filter(
    (song) => song.genre.toLowerCase() === genre.toLowerCase()
  );
};

export const searchSongs = (query: string): Song[] => {
  const lowercaseQuery = query.toLowerCase();
  return mockSongs.filter(
    (song) =>
      song.title.toLowerCase().includes(lowercaseQuery) ||
      song.artist.toLowerCase().includes(lowercaseQuery) ||
      song.album.toLowerCase().includes(lowercaseQuery)
  );
};

export const getRecentSongs = (): Song[] => {
  return mockSongs
    .sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime())
    .slice(0, 10);
};

export const getLikedSongs = (): Song[] => {
  return mockSongs.filter((song) => song.isLiked);
};
